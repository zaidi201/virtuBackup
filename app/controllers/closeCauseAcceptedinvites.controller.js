exports.createcloseacceptedinvites = async (req, res) => {
  // Create comment
  const earnedpoint = {
    closeCauseInviteId: req.body.closeCauseInviteId,
  };
  comments
    .create(earnedpoint)
    .then((data) => {
      res.send(data);
    })
    .catch((err) => {
      res.status(500).send({
        message: err.message || "created",
      });
    });
};
