exports.createsentimenttype = async (req, res) => {
  // Create comment
  const earnedpoint = {
    sentimentName: req.body.sentimentName,
    sentimentIconUrl: req.body.sentimentIconUrl,

    isEnable: 1,
  };
  comments
    .create(earnedpoint)
    .then((data) => {
      res.send(data);
    })
    .catch((err) => {
      res.status(500).send({
        message: err.message || "sentiment type cant be created",
      });
    });
};
