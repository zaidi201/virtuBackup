exports.createPointTitles = async (req, res) => {
  // Create comment
  const earnedpoint = {
    pointTypeId: req.body.pointTypeId,
    nameTitle: req.body.nameTitle,
    pointStartAt: req.body.pointStartAt,
    pointEndAt: req.body.pointEndAt,
    isEnable: 1,
  };
  comments
    .create(earnedpoint)
    .then((data) => {
      res.send(data);
    })
    .catch((err) => {
      res.status(500).send({
        message: err.message || "point titles created",
      });
    });
};
